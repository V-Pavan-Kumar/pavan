public class Operators {
    public static void main(String args[]){
        int a=1,b=2; 
        int sum=a+b;
        int sub=a-b;
        int mul=a*b;
        float div=a/(float)b;
        System.out.println("Addition: "+sum);
        System.out.println("Subtaction: "+sub);
        System.out.println("Multiplication: "+mul);
        System.out.println("Division: "+div);
    }
}